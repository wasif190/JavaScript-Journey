// An array is a special type of object that allows you to store multiple values in a single variable.
// Arrays can hold values of different data types, including numbers, strings, and even other arrays
// Each value in an array is identified by an index, starting from 0 for the first element.

// Creating an array
let fruits = ['apple', 'orange', 'banana', 'grape'];

// Accessing elements
console.log(fruits[0]); // Output: 'apple'
console.log(fruits[1]); // Output: 'orange'
console.log(fruits[2]); // Output: 'banana'
console.log(fruits[3]); // Output: 'grape'

// Modifying an element
fruits[1] = 'pear';
console.log(fruits); // Output: ['apple', 'pear', 'banana', 'grape']

// Getting the length of an array
console.log(fruits.length); // Output: 4
