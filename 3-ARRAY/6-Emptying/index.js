// Setting the length of the array to 0 
let arr = [1, 2, 3, 4] 
arr.length = 0; 
console.log(arr); // [] 

// Using the 'splice()' method 
let arr1 = [1, 2, 3]; 
arr1.splice(0, arr1.length); 
console.log(arr1) 

// Assigning an empty array to the existing array variable '
let arr2 = [1, 2, 3]; 
arr2 = []; 
