let x = 10;
let y = 26;

console.log(`x + y is ${x + y}`);
console.log(`x - y is ${x - y}`);
console.log(`x * y is ${x * y}`);
console.log(`x / y is ${x / y}`);
// Remainder (Modulus) - Returns the remainder of the division
console.log(`x % y is ${x % y}`); 
// Exponentiation - Raises the left operand to the power of the right operand
console.log(`x ** y is ${x ** y}`);

// Increment (++) - Increases the value of the operand by 1
console.log(`x is ${x}`); // Outputs: x is 10
console.log(`Fist increment then print - ++x, x is ${++x}`); // Output: 11
console.log(`First print then increment - y++, y is ${y++}`); // Output: 26
console.log(y); // Output: 27

// Decrement (--) - Decreases the value of the operand by 1
console.log(`x is ${x}`); // 11
console.log(`y is ${y}`); // 27
console.log(`Fist descrement then print -> --x, x is ${--x}`); // Outputs: 10
console.log(`First print then descrement -> y--, y is ${y--}`); // Output: 27
console.log(y); // Output: 26
