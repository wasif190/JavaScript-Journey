// Assignment (=) 
// Assigns the value of the right operand to the left operand
let x = 5; // x is assigned the value 5


// Addition Assignment (+=) 
// Adds the value of the right operand to the left operand and assigns the result to the left operand
let y = 10;
y += 3; // Equivalent to y = y + 3; // y is now 13

// Subtraction Assignment (-=) 
// Subtracts the value of the right operand from the left operand and assigns the result to the left operand
let z = 8;
z -= 4; // Equivalent to z = z - 4; // z is now 4

// Multiplication Assignment (*=) 
// Multiplies the left operand by the value of the right operand and assigns the result to the left operand
let a = 6;
a *= 2; // Equivalent to a = a * 2; // a is now 12

// Division Assignment (/=) 
// Divides the left operand by the value of the right operand and assigns the result to the left operand
let b = 16;
b /= 4; // Equivalent to b = b / 4; // b is now 4

// Remainder Assignment (Modulus) (%=) 
// Calculates the remainder of the division of the left operand by the right operand and assigns the result to the left operand
let c = 17;
c %= 5; // Equivalent to c = c % 5; // c is now 2

// Exponentiation Assignment (**=)  
// Raises the left operand to the power of the right operand and assigns the result to the left operand
let d = 3;
d **= 4; // Equivalent to d = d ** 4; // d is now 81
